// Função para obter imagens de um veículo com fallback
export const getVehicleImages = (vehicleId: string): string[] => {
  const config = vehicleImages[vehicleId as keyof typeof vehicleImages];
  
  if (config) {
    return [config.main, ...config.gallery];
  }

  // Fallback para imagens genéricas se não encontrar configuração específica
  const fallbackImages = [
    'https://images.pexels.com/photos/170811/pexels-photo-170811.jpeg?auto=compress&cs=tinysrgb&w=800',
    'https://images.pexels.com/photos/1149137/pexels-photo-1149137.jpeg?auto=compress&cs=tinysrgb&w=800',
    'https://images.pexels.com/photos/1429775/pexels-photo-1429775.jpeg?auto=compress&cs=tinysrgb&w=800'
  ];

  return fallbackImages;
};

// Função para verificar se uma imagem existe (para uso futuro)
export const checkImageExists = async (url: string): Promise<boolean> => {
  try {
    const response = await fetch(url, { method: 'HEAD' });
    return response.ok;
  } catch {
    return false;
  }
};

